    private void tabloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabloMouseClicked
        DefaultTableModel model= (DefaultTableModel) tablo.getModel();
        
        int secili_row=tablo.getSelectedRow();
        ad.setText(model.getValueAt(secili_row, 0).toString());
        soyad.setText(model.getValueAt(secili_row, 1).toString());
        yas.setText(model.getValueAt(secili_row, 2).toString());
        birim.setSelectedItem(model.getValueAt(secili_row, 3).toString());
        dr_bilgisi.setText(model.getValueAt(secili_row, 4).toString());
        tarih.setText(model.getValueAt(secili_row, 5).toString());
        
    }//GEN-LAST:event_tabloMouseClicked

