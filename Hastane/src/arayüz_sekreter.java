    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
       
        Tablo3 tb3=new Tablo3();
        Tablo2 tb2=new Tablo2();
        mesaj.setText("");
        DefaultTableModel model= (DefaultTableModel) tablo.getModel();//tür dönüşümü yapılır.
        
        if(ad.getText().trim().equals(""))//baştaki ve sondaki boşlukları silen metod (trim)
        {
            mesaj.setText("hasta ismi boş bırakılamaz...");
        }
        else{
            Object[] eklenecek={ad.getText(),soyad.getText(),yas.getText(),birim.getSelectedItem().toString(),dr_bilgisi.getText(),tarih.getText()};
            
            model.addRow(eklenecek);//object array eklenmiş oldu...//tablomuzu oluşturmuş olduk...
        }
        String adı=ad.getText();
        String soyadı=soyad.getText();
        String birimi=birim.getSelectedItem().toString();
        String doktor=dr_bilgisi.getText();
        String tarihi=tarih.getText();
        int yası = Integer.parseInt(yas.getText());//stringi integer değere çevirme...
        tb2.ekle(adı, soyadı, yası);
        tb3.ekle(adı, soyadı, birimi, doktor, tarihi);
        
        
    }//GEN-LAST:event_ekleActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        DefaultTableModel model= (DefaultTableModel) tablo.getModel();
        Tablo3 tb3=new Tablo3();
        Tablo2 tb2=new Tablo2();
       int secili_row=tablo.getSelectedRow();
       if(secili_row==-1)
       {
           if(tablo.getRowCount()==0)
           {
               mesaj.setText("randevu tablosu boş");
           }
           else{
               mesaj.setText("lütfen randevusu silinecek hastayı seçiniz");
           }
       }
       else{
           
           model.removeRow(secili_row);
           mesaj.setText("randevu başarıyla silindi");
           
           String adı=ad.getText();
           String soyadı=soyad.getText();
           tb2.sil(adı, soyadı);
           tb3.sil(adı, soyadı);
           
           
           
       }
    }//GEN-LAST:event_silActionPerformed

    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        mesaj.setText("");
        DefaultTableModel model= (DefaultTableModel) tablo.getModel();//model oluşumu...
        int secili_row=tablo.getSelectedRow();
        if(secili_row==-1)
        {
            if(tablo.getRowCount()==0)
            {
                mesaj.setText("randevu tablosu şu anda boş");
            }
            else{
                mesaj.setText("lütfen güncellenecek hastayı seçin");
            }
        }
        else{
            model.setValueAt(ad.getText(), secili_row, 0);
            model.setValueAt(soyad.getText(), secili_row, 1);
            model.setValueAt(yas.getText(), secili_row, 2);
            model.setValueAt(birim.getSelectedItem(), secili_row, 3);
            model.setValueAt(dr_bilgisi.getText(), secili_row, 4);
            model.setValueAt(tarih.getText(), secili_row, 5);
            
            mesaj.setText("hasta başarıyla güncellendi");
        }
    }//GEN-LAST:event_guncelleActionPerformed

